CREATE TABLE PRODUCTO (
PRO_ID NUMBER,
PRO_NOMBRES VARCHAR2(150) NOT NULL,
PRO_DESCRIPCIONES VARCHAR(350) NOT NULL,
PRO_STOCK NUMBER CHECK(PRO_STOCK>=0),
PRO_PRECIO_UNIDADES NUMBER(2,8)CHECK(PRO_PRECIO_UNIDADES>=0) NOT NULL,
PRO_DESCUENTO NUMBER(2,8) CHECK(PRO_DESCUENTO>=0) NOT NULL,
PRO_TALLA VARCHAR2(8) NOT NULL,
PRO_ESTADO VARCHAR2(24) NOT NULL,
CTT_ID NUMBER,
CTR_ID NUMBER,
MTR_ID NUMBER NOT NULL,

CONSTRAINT PK_PRO_ID PRIMARY KEY(PRO_ID),
CONSTRAINT FK_CTT_PRO FOREIGN KEY(CTT_ID) REFERENCES CATEGORIA_TEMATICA(CTT_ID) ON DELETE CASCADE,
CONSTRAINT FK_CTR_PRO FOREIGN KEY(CTR_ID) REFERENCES CATEGORIA_ROPA(CTR_ID) ON DELETE CASCADE,
CONSTRAINT FK_MTR_PRO FOREIGN KEY(MTR_ID) REFERENCES MATERIALES(MTR_ID) ON DELETE CASCADE,
CONSTRAINT CK_ESTADO CHECK(PRO_ESTADO IN('Activo','Inactivo'))
);

COMMENT ON COLUMN PRODUCTO.PRO_ID IS 'Id del producto';
COMMENT ON COLUMN PRODUCTO.PRO_NOMBRES IS 'Nombre del producto';
COMMENT ON COLUMN PRODUCTO.PRO_DESCRIPCIONES IS 'Descripcion del producto';
COMMENT ON COLUMN PRODUCTO.PRO_STOCK IS 'Cantidad en stock';
COMMENT ON COLUMN PRODUCTO.PRO_PRECIO_UNIDADES IS 'Precio por unidad';
COMMENT ON COLUMN PRODUCTO.PRO_DESCUENTO IS 'Descuento que le apique al producto';
COMMENT ON COLUMN PRODUCTO.PRO_TALLA IS 'Talla del producto';
COMMENT ON COLUMN PRODUCTO.PRO_ESTADO IS 'Estado del producto, activo es cuando esta a la venta, inactivo es en caso no haya productos en stock o no puedan cumplir con la entrega o elaboracion del producto en ese momoento';
COMMENT ON COLUMN PRODUCTO.CTT_ID IS 'Id de la Categoria temarica ';
COMMENT ON COLUMN PRODUCTO.CTR_ID IS 'Id de la categoria de ropa';
COMMENT ON COLUMN PRODUCTO.MTR_ID IS 'Id de el material con que esta hecho el producto';

