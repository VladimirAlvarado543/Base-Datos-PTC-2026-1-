CREATE TABLE PEDIDO(
PED_ID NUMBER,
PED_FECHAS DATE,
PED_PRECIO_TOTALES NUMBER(2,8) CHECK(PED_PRECIO_TOTALES>=0) NOT NULL,
PED_TOTAL_DESCUENTOS NUMBER(2,8) CHECK(PED_TOTAL_DESCUENTOS>=0) NOT NULL,
PED_ESTADO_COMPRAS VARCHAR2(24)NOT NULL,
PED_SOLICITUDES VARCHAR2(24) NOT NULL,
UBC_ID NUMBER NOT NULL,
USR_ID NUMBER NOT NULL,


CONSTRAINT CK_PED_ESTADO CHECK(PED_ESTADO_COMPRAS IN('Carrito','Pendiente','Aprobado')),
CONSTRAINT CK_PED_SOLICITUD CHECK(PED_SOLICITUDES IN('Pendiente','Aprobado')),
CONSTRAINT PK_PED_ID PRIMARY KEY(PED_ID),
CONSTRAINT FK_UBC_PED FOREIGN KEY(UBC_ID) REFERENCES UBICACION_CLIENTE(UBC_ID) ON DELETE CASCADE,
CONSTRAINT FK_USR_PED FOREIGN KEY(USR_ID) REFERENCES USUARIO(USR_ID) ON DELETE CASCADE
);

COMMENT ON COLUMN PEDIDO.PED_ID IS 'Id del pedido';
COMMENT ON COLUMN PEDIDO.PED_FECHAS IS 'Fecha en que se entrega el pedido';
COMMENT ON COLUMN PEDIDO.PED_PRECIO_TOTALES IS 'Precio total del pedido';
COMMENT ON COLUMN PEDIDO.PED_TOTAL_DESCUENTOS IS 'Precio total del pedido aplicando los descuentos';
COMMENT ON COLUMN PEDIDO.PED_ESTADO_COMPRAS IS 'Estado de compra, se utilizara esta misma tabla para el carrito de compras, una vez el cliente confirme la compra debera esperar a que el pedido sea aprovado por un administrador';
COMMENT ON COLUMN PEDIDO.PED_SOLICITUDES IS 'Estado de la solicitud de aprovacion, ya sea aprovado o pendiente';
COMMENT ON COLUMN PEDIDO.UBC_ID IS 'Id de la ubicacion de entrega';
COMMENT ON COLUMN PEDIDO.USR_ID IS 'Id del usuario que aprovo el pedido';

