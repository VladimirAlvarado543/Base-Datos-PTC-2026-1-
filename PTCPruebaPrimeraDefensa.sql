CREATE TABLE ROL(
ROL_id NUMBER,
ROL_nombres VARCHAR2(20) UNIQUE NOT NULL,
CONSTRAINT PK_ROL_ID PRIMARY KEY(ROL_id),
CONSTRAINT NOMBRESROL CHECK(ROL_nombres IN ('Administrador','Empleado','Repartidor'))
);

COMMENT ON COLUMN ROL.ROL_ID IS 'Id del rol';
COMMENT ON COLUMN ROL.ROL_NOMBRES IS 'Nomnbre interno que tendra el rol dentro del sistema';

