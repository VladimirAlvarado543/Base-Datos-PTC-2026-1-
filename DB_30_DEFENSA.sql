/*Trigger para descontar stock al aprovarse una venta*/
CREATE OR REPLACE TRIGGER TRG_DESCONTAR_STOCK
AFTER UPDATE OF PED_ESTADO_COMPRAS ON PEDIDO
FOR EACH ROW
DECLARE
V_STOCK PRODUCTO.PRO_STOCK%TYPE;
BEGIN
IF :OLD.PED_ESTADO_COMPRAS<>'Aprobado'
AND :NEW.PED_ESTADO_COMPRAS='Aprobado' THEN
FOR V_DETALLE IN(
SELECT PRO_ID,DTP_CANTIDADES
FROM DETALLE_PEDIDO
WHERE PED_ID=:NEW.PED_ID
)
LOOP
SELECT PRO_STOCK
INTO V_STOCK
FROM PRODUCTO
WHERE PRO_ID=V_DETALLE.PRO_ID;
IF V_DETALLE.DTP_CANTIDADES>V_STOCK THEN
RAISE_APPLICATION_ERROR(-20006,'Stock insuficiente para aprobar el pedido');
END IF;
UPDATE PRODUCTO
SET PRO_STOCK=PRO_STOCK-V_DETALLE.DTP_CANTIDADES
WHERE PRO_ID=V_DETALLE.PRO_ID;
END LOOP;
END IF;
END;
