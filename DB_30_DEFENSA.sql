/*Trigger para verificar que no se pueda realizar un insert de detalle pedido a un pedido que no tenga el estado de carrito*/
CREATE OR REPLACE TRIGGER TRG_INSERT_DETALLE_PEDIDO
BEFORE INSERT ON DETALLE_PEDIDO
FOR EACH ROW
DECLARE
V_ESTADO PEDIDO.PED_ESTADO_COMPRAS%TYPE;
V_STOCK PRODUCTO.PRO_STOCK%TYPE;
BEGIN
SELECT PED_ESTADO_COMPRAS
INTO V_ESTADO
FROM PEDIDO
WHERE PED_ID=:NEW.PED_ID;
IF V_ESTADO<>'Carrito' THEN
RAISE_APPLICATION_ERROR(-20012,'Solo se pueden agregar productos en carrito');
END IF;
SELECT PRO_STOCK
INTO V_STOCK
FROM PRODUCTO
WHERE PRO_ID=:NEW.PRO_ID;
IF :NEW.DTP_CANTIDADES>V_STOCK THEN
RAISE_APPLICATION_ERROR(-20002,'La cantidad supera el stock disponible');
END IF;
END;
/
/*Trigger para aumentar al total del pedido cuando se añado un producto (en este caso un registro de detalle pedido)*/
CREATE OR REPLACE TRIGGER TRG_INSERT_TOTAL_PEDIDO
AFTER INSERT ON DETALLE_PEDIDO
FOR EACH ROW
DECLARE
V_PRECIO PRODUCTO.PRO_PRECIO_UNIDADES%TYPE;
V_DESCUENTO PRODUCTO.PRO_DESCUENTOS%TYPE;
BEGIN
SELECT PRO_PRECIO_UNIDADES,PRO_DESCUENTOS
INTO V_PRECIO,V_DESCUENTO
FROM PRODUCTO
WHERE PRO_ID=:NEW.PRO_ID;
UPDATE PEDIDO
SET PED_PRECIO_TOTALES=PED_PRECIO_TOTALES+(V_PRECIO*:NEW.DTP_CANTIDADES),
PED_TOTAL_DESCUENTOS=PED_TOTAL_DESCUENTOS+(V_DESCUENTO*:NEW.DTP_CANTIDADES),
PED_TOTAL_FINALES=PED_TOTAL_FINALES+((V_PRECIO-V_DESCUENTO)*:NEW.DTP_CANTIDADES)
WHERE PED_ID=:NEW.PED_ID;
END;
/
/*Trigger para verificar que no se pueda actualizar informacion de detalle pedido si el estado de compras no es carrito*/
CREATE OR REPLACE TRIGGER TRG_VALIDAR_UPDATE_DETALLE
BEFORE UPDATE ON DETALLE_PEDIDO
FOR EACH ROW
DECLARE
V_ESTADO PEDIDO.PED_ESTADO_COMPRAS%TYPE;
V_STOCK PRODUCTO.PRO_STOCK%TYPE;
V_DIFERENCIA NUMBER;
BEGIN
SELECT PED_ESTADO_COMPRAS
INTO V_ESTADO
FROM PEDIDO
WHERE PED_ID=:OLD.PED_ID;
IF V_ESTADO<>'Carrito' THEN
RAISE_APPLICATION_ERROR(-20013,'Solo se puede modificar en carrito');
END IF;
SELECT PRO_STOCK
INTO V_STOCK
FROM PRODUCTO
WHERE PRO_ID=:OLD.PRO_ID;
V_DIFERENCIA:=:NEW.DTP_CANTIDADES-:OLD.DTP_CANTIDADES;
IF V_DIFERENCIA>V_STOCK THEN
RAISE_APPLICATION_ERROR(-20004,'Stock insuficiente');
END IF;
END;
/
/*Trigger para actualizar el total a pagar de un pedido si se añaden mas productos del mismo tipo*/
CREATE OR REPLACE TRIGGER TRG_UPDATE_TOTAL_PEDIDO
AFTER UPDATE ON DETALLE_PEDIDO
FOR EACH ROW
DECLARE
V_PRECIO PRODUCTO.PRO_PRECIO_UNIDADES%TYPE;
V_DESCUENTO PRODUCTO.PRO_DESCUENTOS%TYPE;
BEGIN
SELECT PRO_PRECIO_UNIDADES,PRO_DESCUENTOS
INTO V_PRECIO,V_DESCUENTO
FROM PRODUCTO
WHERE PRO_ID=:NEW.PRO_ID;
UPDATE PEDIDO
SET PED_PRECIO_TOTALES=PED_PRECIO_TOTALES-(V_PRECIO*:OLD.DTP_CANTIDADES)+(V_PRECIO*:NEW.DTP_CANTIDADES),
PED_TOTAL_DESCUENTOS=PED_TOTAL_DESCUENTOS-(V_DESCUENTO*:OLD.DTP_CANTIDADES)+(V_DESCUENTO*:NEW.DTP_CANTIDADES),
PED_TOTAL_FINALES=PED_TOTAL_FINALES-((V_PRECIO-V_DESCUENTO)*:OLD.DTP_CANTIDADES)+((V_PRECIO-V_DESCUENTO)*:NEW.DTP_CANTIDADES)
WHERE PED_ID=:NEW.PED_ID;
END;
/
